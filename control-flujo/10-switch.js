// Switch es casi lo mismo que IF/ELSE pero se usa 
// para casos menos complejos como Comparar una variable 
// contra varios valores exactos.

let frutas = "5";

switch(frutas){

    case "1":
        console.log("Es una pera");
        break;
    
     case "2":
        console.log("Es un mango");
        break;

     case "3":
        console.log("Es una banana");
        break;

        default:
            console.log("Digite un numero correcto");
        
}

