let numero = 10;
 
//Operadores relacionales
console.log("Operadores relacionales")
console.log(numero < 15);
console.log(numero <= 15);
console.log(numero > 15);
console.log(numero >= 15);

console.log("Operadores de igualdad")
console.log(numero == 10); // si 10 es igualdad a 10
console.log(numero !=10); //si 10 es distinto de 10
console.log(numero === "10"); //compara si tiene mismo valor y mismo tipo dato
console.log(numero !== "10"); //compara y dice si tiene distinto tipo de dato por eso da true

//consejo: usar mejor los operadores de igualdad de === y !== porque son menos propensos a errores.