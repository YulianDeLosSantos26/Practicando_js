let valor1 =5;
let valor2 = 6;

//operadores aritmeticos
console.log( valor1 + valor2, ' adicion o suma');
console.log( valor1 - valor2, ' resta');
console.log( valor1 / valor2, ' division');
console.log( valor1 * valor2, ' multiplicacion');
console.log( valor1 % valor2, ' modulo');
console.log( valor1 ** valor2, ' exponeciacion');

